#pragma mark <UICollectionViewDataSource>

- (NSInteger)numberOfSectionsInCollectionView:(UICollectionView *)collectionView {
    return 1;
}


- (NSInteger)collectionView:(UICollectionView *)collectionView numberOfItemsInSection:(NSInteger)section {
    return 7;
}

- (UICollectionViewCell *)collectionView:(UICollectionView *)collectionView cellForItemAtIndexPath:(NSIndexPath *)indexPath {
    IGMBanksListCollectionViewCell *cell = [collectionView dequeueReusableCellWithReuseIdentifier:reuseIdentifier forIndexPath:indexPath];
    
    // Configure the cell
    
    cell.titleLabel.text = [NSString stringWithFormat:@"Bank %ld", indexPath.row + 1];
    
    return cell;
}

#pragma mark <UICollectionViewDelegate>

- (CGSize)collectionView:(UICollectionView *)collectionView layout:(UICollectionViewLayout *)collectionViewLayout sizeForItemAtIndexPath:(NSIndexPath *)indexPath
{
    CGFloat screenWidth = self.collectionView.frame.size.width;
    
    // 30.f - сумма отступов слева, спарава и между ячейками (по 10 px)
    // 2.f по 2 ячейки в ряд
    CGFloat cellSize = (screenWidth - 30.f) / 2.f;
    
    return CGSizeMake(cellSize, cellSize);
}

